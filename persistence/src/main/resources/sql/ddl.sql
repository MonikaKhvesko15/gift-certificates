-- Database: certificates

-- DROP DATABASE certificates;

CREATE DATABASE certificates
    WITH
    OWNER = postgres
    ENCODING = 'UTF8'
    LC_COLLATE = 'English_United States.1252'
    LC_CTYPE = 'English_United States.1252'
    TABLESPACE = pg_default
    CONNECTION LIMIT = -1;

SET TIME ZONE 'UTC';

SET datestyle TO "ISO";